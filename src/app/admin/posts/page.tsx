'use client'

import Link from 'next/link'
import { useEffect, useState } from 'react'
import { useSupabaseSession } from '@/app/_hooks/useSupabaseSession'
import { Post } from '@/types/post'

export default function Page() {
  const [posts, setPosts] = useState<Post[]>([])
  const [isLoading, setIsLoading] = useState(true)
  const { token } = useSupabaseSession()

  useEffect(() => {
    if (!token) return

    const fetcher = async () => {
      const res = await fetch('/api/admin/posts', {
        headers: {
          'Content-Type': 'application/json',
          Authorization: token, // üëà Header „Å´ token „Çí‰ªò‰∏é„ÄÇ
        },
      })
       if (!res.ok) {
    console.error('Fetch failed:', res.status, res.statusText)
    return
  }
    const text = await res.text()
    if (!text) {
        console.error('„É¨„Çπ„Éù„É≥„Çπ„ÅåÁ©∫„Åß„Åô')
        return
      }
    const { posts } = JSON.parse(text)


      setPosts([...posts])
      setIsLoading(false)
    }

    fetcher()
  }, [token])

  return (
    <div className="">
      <div className="flex justify-between items-center mb-8">
        <h1 className="text-xl font-bold">Ë®ò‰∫ã‰∏ÄË¶ß</h1>
        <button className="bg-blue-500 hover:bg-blue-700 text-white font-bold py-2 px-4 rounded">
          <Link href="/admin/posts/new">Êñ∞Ë¶è‰ΩúÊàê</Link>
        </button>
      </div>

      <div className="">
        {isLoading && <div>loading...</div>}
        {!isLoading && posts.length === 0 && <div>Ë®ò‰∫ã„Åå„ÅÇ„Çä„Åæ„Åõ„Çì</div>}
        {!isLoading &&
          posts.map((post) => {
            return (
              <Link href={`/admin/posts/${post.id}`} key={post.id}>
                <div className="border-b border-gray-300 p-4 hover:bg-gray-100 cursor-pointer">
                  <div className="text-xl font-bold">{post.title}</div>
                  <div className="text-gray-500">
                    {new Date(post.createdAt).toLocaleDateString()}
                  </div>
                </div>
              </Link>
            )
          })}
      </div>
    </div>
  )
}